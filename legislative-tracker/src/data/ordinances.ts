export const getOrdinanceById = (id: string): Ordinance | undefined => {
  return ordinances.find(ordinance => ordinance.id === id);
};

export const getActiveOrdinances = (): Ordinance[] => {
  return ordinances.filter(ordinance => ordinance.status === 'Active');
};

export const getOrdinancesByCategory = (category: string): Ordinance[] => {
  return ordinances.filter(ordinance => ordinance.category === category);
};

export const getOrdinancesByAuthor = (author: string): Ordinance[] => {
  return ordinances.filter(ordinance => ordinance.author === author);
};

export const getRecentOrdinances = (limit: number = 10): Ordinance[] => {
  return ordinances
    .sort((a, b) => new Date(b.dateEnacted).getTime() - new Date(a.dateEnacted).getTime())
    .slice(0, limit);
};