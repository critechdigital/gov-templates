export const getResolutionById = (id: string): Resolution | undefined => {
  return resolutions.find(resolution => resolution.id === id);
};

export const getResolutionsByType = (type: string): Resolution[] => {
  return resolutions.filter(resolution => resolution.type === type);
};

export const getResolutionsByAuthor = (author: string): Resolution[] => {
  return resolutions.filter(resolution => resolution.author === author);
};

export const getRecentResolutions = (limit: number = 10): Resolution[] => {
  return resolutions
    .sort((a, b) => new Date(b.dateEnacted).getTime() - new Date(a.dateEnacted).getTime())
    .slice(0, limit);
};