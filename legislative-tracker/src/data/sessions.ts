export const getSessionById = (id: string): Session | undefined => {
  return sessions.find(session => session.id === id);
};

export const getSessionsByStatus = (status: string): Session[] => {
  return sessions.filter(session => session.status === status);
};

export const getSessionsByType = (type: string): Session[] => {
  return sessions.filter(session => session.type === type);
};

export const getSessionsByDateRange = (startDate: string, endDate: string): Session[] => {
  return sessions.filter(session => 
    session.date >= startDate && session.date <= endDate
  );
};

export const getUpcomingSessions = (limit: number = 5): Session[] => {
  const today = new Date().toISOString().split('T')[0];
  return sessions
    .filter(session => session.date >= today && session.status === 'Scheduled')
    .sort((a, b) => new Date(a.date).getTime() - new Date(b.date).getTime())
    .slice(0, limit);
};

export const getCompletedSessions = (limit: number = 10): Session[] => {
  return sessions
    .filter(session => session.status === 'Completed')
    .sort((a, b) => new Date(b.date).getTime() - new Date(a.date).getTime())
    .slice(0, limit);
};